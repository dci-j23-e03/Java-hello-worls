package dci.exercise;

public class Main {
    public static void main(String[] args) {
        System.out.println("Hello World!");
        System.out.print("Integer value: ");
        System.out.println(30);
        System.out.print("Long value: ");
        System.out.println(50L);
        System.out.print("Float value: ");
        System.out.println(4.0f);
        System.out.print("Double value: ");
        System.out.println(10.99);
        System.out.print("Boolean value: ");
        System.out.println(true);
        System.out.print("String value: ");
        System.out.println("so literally what ever text you put here will be printed");
    }
}
